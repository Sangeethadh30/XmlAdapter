package com.adapter.model;

/**  
* Attributes.java - a simple attribute class mapped to attribute field of RequestModel json.  
* @author  Vikas Singh
* @version 1.0 
*/ 
public class Attributes {
	private String attrName;
	private String attrNewValue;
	
	public String getAttrName() {
		return attrName;
	}
	public void setAttrName(String attrName) {
		this.attrName = attrName;
	}
	public String getAttrNewValue() {
		return attrNewValue;
	}
	public void setAttrNewValue(String attrNewValue) {
		this.attrNewValue = attrNewValue;
	}

}
